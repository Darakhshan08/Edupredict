const mongoose = require("mongoose");
const StudentSchema = mongoose.Schema(
  {
    student_id: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

module.exports = mongoose.model("student", StudentSchema);
